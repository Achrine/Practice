namespace functions {
    int subtraction(int a, int b){
        int z = 0;
        z = a - b;
        return(z);
    }
}
int addition(int a, int b){
    int sum = 0;
    sum = a + b;
    return(sum);
}
int multiple(int a, int b){
    int product = a * b;
    return(product);
}
int square(int a){
    int z = a * a;
    return (z);
}



